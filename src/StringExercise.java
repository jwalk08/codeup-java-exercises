public class StringExercise {
    public static void main(String[] args) {
        String message = "We don't need no education\nWe don't need not thought control";
        System.out.println(message);

        message = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(message);

        message = "In windows, the main drive is usually C:\\";
        System.out.println(message);
        message = "I can do backslashes \\, double backlashes \\\\," +
                "and the amazing triple backslash";
    }
}
